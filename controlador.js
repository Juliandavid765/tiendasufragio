    foto.classList.add("w-100" , "img-fluid")
    foto.src=producto.foto
    let columna=document.createElement("div")
    columna.classList.add("col")
    let tarjeta=document.createElement("div")
    let nombreProductos=document.createElement("h5")
    nombreProductos.classList.add("text-center")
    nombreProductos.textContent=producto.nombre
    tarjeta.classList.add("card")
    let precio=document.createElement("h5")
    precio.classList.add("text-center")
    precio.classList.add("bg-success")
    precio.textContent="$"+producto.precio
    let popularidad=document.createElement("h5")
    popularidad.classList.add("text-center")
    popularidad.classList.add("bg-dark")
    popularidad.classList.add("text-white")
    popularidad.textContent=producto.popularidad
    let pais=document.createElement("h5")
    pais.classList.add("text-center")
    pais.textContent=producto.pais
    let estado=document.createElement("h5")
    if (producto.estado==true){
        estado.classList.add("text-center")
        estado.textContent="Nuevo"
    }else{
        estado.classList.add("text-center")
        estado.textContent="Usado"
    }
    tarjeta.appendChild(foto)
    tarjeta.appendChild(nombreProductos)
    tarjeta.appendChild(precio)
    tarjeta.appendChild(estado)
    tarjeta.appendChild(popularidad)
    tarjeta.appendChild(pais)
    columna.appendChild(tarjeta)
    contenedor.appendChild(columna)







